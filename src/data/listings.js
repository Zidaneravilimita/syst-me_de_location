export function getServiceLabel(serviceType) {
  return serviceTypes.find((s) => s.id === serviceType)?.label ?? serviceType
}

export function getCategoryLabel(serviceType, category) {
  const cats = subcategories[serviceType]
  if (!cats) return category
  return cats.find((c) => c.id === category)?.label ?? category
}
